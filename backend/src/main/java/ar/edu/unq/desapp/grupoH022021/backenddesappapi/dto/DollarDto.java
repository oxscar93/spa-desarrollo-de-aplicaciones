package ar.edu.unq.desapp.grupoH022021.backenddesappapi.dto;

public class DollarDto {
	private String compra;
	private String venta;
	private String fecha;
	private String variacion;
	private String classVariacion;
	
	public String getCompra() {
		return compra;
	}
	public void setCompra(String compra) {
		this.compra = compra;
	}
	public String getVenta() {
		return venta;
	}
	public void setVenta(String venta) {
		this.venta = venta;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getVariacion() {
		return variacion;
	}
	public void setVariacion(String variacion) {
		this.variacion = variacion;
	}
	public String getClassVariacion() {
		return classVariacion;
	}
	public void setClassVariacion(String classVariacion) {
		this.classVariacion = classVariacion;
	}
	
	

}
